#comando for
for contador in range(0,4): 
    print ("\n", contador) # 0, 1, 2, 3

for cont in range(1,4): 
    print ("\n", cont) # 1, 2, 3

for n in range(10, 22, 2): #começa em 10, termina em 22 e avança de 2 em 2
    print ("\n", n) # 10, 12, 14, 16, 18, 20

for i in range (10, 0, -1): #descrente
    print("\n", i) # 10, 8, 6, 4, 2, 1



